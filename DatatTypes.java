public class DatatTypes{
    public static void main(String args[]){

        //BYTE 

        System.err.println("Byte size is : " + Byte.BYTES);
        System.out.println("The range of byte is : " + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);

        //SHORT
        System.err.println("Short size is : " + Short.BYTES);
        System.out.println("The range of short is : " + Short.MIN_VALUE + " to " + Short.MAX_VALUE);

        //INTEGER
        System.err.println("Integer size is : " + Integer.BYTES);
        System.out.println("The range of integer is : " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);

        //FLOAT
        System.err.println("Float size is : " + Float.BYTES);
        System.out.println("The range of float is : " + Float.MIN_VALUE + " to " + Float.MAX_VALUE);

        //LONG
        System.err.println("Long size is : " + Long.BYTES);
        System.out.println("The range of long is : " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);

        //DOUBLE
        System.err.println("Double size is : " + Double.BYTES);
        System.out.println("The range of double is : " + Double.MIN_VALUE + " to " + Double.MAX_VALUE);

        //CHAR
        System.err.println("Character size is : " + Character.BYTES);
        System.out.println("The range of character is : " + (int)Character.MIN_VALUE + " to " + (int)Character.MAX_VALUE);

    }
}